    private void btnIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIdActionPerformed
        // TODO add your handling code here:
        try {
            Statement st = Lconnection.createStatement();
            ResultSet rs = st.executeQuery("Select * from tbbuku" + " where Kode='" + txtkodebuku.getText() + "'");
            if (rs.next()) {
                btndelete.setEnabled(true);
                txtnama.setText(rs.getString("Nama"));
                txtpenerbit.setText(rs.getString("Penerbit"));
                txtpengarang.setText(rs.getString("Pengarang"));
                txttahun.setText(rs.getString("Tahun"));
                txtkota.setText(rs.getString("Kota"));
                txtjudul.setText(rs.getString("Judul"));
                txtkodebuku.setText(rs.getString("Kode"));
            } else {
                JOptionPane.showMessageDialog(this,"ID: Salah", "Informasi", JOptionPane.INFORMATION_MESSAGE);

               btndelete.setEnabled(false);
               txtkodebuku.setText("");
               txtnama.setText("");
               txtpenerbit.setText("");
               txtpengarang.setText("");
               txttahun.setText("");
               txtkota.setText("");
               txtjudul.setText("");
               txtnama.requestFocus();
                
            }
        } catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());

        }
    }//GEN-LAST:event_btnIdActionPerformed

