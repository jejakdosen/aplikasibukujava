    private void btninputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninputActionPerformed
        // TODO add your handling code here:
        
      
        try {
            Class.forName("com.mysql.jdbc.Driver");

            url="jdbc:mysql://"+ host +"/"+ db + "?user=" + user + "&password="+ pswd;
            Connection conn=DriverManager.getConnection(url);

            PreparedStatement pStatement = conn.prepareStatement("INSERT INTO tbbuku(Kode, Nama, Penerbit, Pengarang, Tahun, Kota, Judul)" + "VALUES (?,?,?,?,?,?,?)");

            pStatement.setString(1, txtkodebuku.getText());
            pStatement.setString(2, txtnama.getText());
            pStatement.setString(3, txtpenerbit.getText());
            pStatement.setString(4,  txtpengarang.getText());
            pStatement.setString(5, txttahun.getText());
            pStatement.setString(6, txtkota.getText());
            pStatement.setString(7, txtjudul.getText());

            if (pStatement.executeUpdate()>0)
                JOptionPane.showMessageDialog(this,
                   "Penambahan sukses", "Informasi",
                   JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(this,
                   "Penambahan gagal", "Informasi",
                   JOptionPane.INFORMATION_MESSAGE);

            pStatement.close();
            conn.close();

            txtkodebuku.setText("");
            txtnama.setText("");
            txtpenerbit.setText("");
            txtpengarang.setText("");
            txttahun.setText("");
            txtkota.setText("");
            txtjudul.setText("");
       }

       catch(ClassNotFoundException e) {
            System.out.println("jdbc.Driver tidak ditemukan");
       }

       catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());

      }
    }//GEN-LAST:event_btninputActionPerformed

